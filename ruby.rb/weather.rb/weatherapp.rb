weather = 'sunny'
case weather
when 'rainy'
puts "Make sure you bring an umbrella"
when 'windy'
puts "If you have long hair, bring an elastic! If you wear a hat, hold on to it!"
when 'sunny'
puts "Better bring some shades, dudette"
when 'cloudy'
puts "it might be a little chilly without the sun. Bring a sweater"
when 'snowy'
puts "You're gonna need some snow pants and boots"
else
puts "Hmmm,  dont know what you should wear in that kind of weather"
end